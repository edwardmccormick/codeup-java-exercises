import java.util.Scanner;

public class StringExercise {
    public static void main(String[] args) {
        String thewall = "We don't need no education \nWe don't need no thought control";
        System.out.println(thewall);
        String escape1 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(escape1);
        String escape2 = "In windows, the main drive is usually C:\\\\";
        System.out.println(escape2);
        String escape3 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(escape3);
    }
}